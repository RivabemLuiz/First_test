frutas = ["banana", "maçã", "alecrim"]

for i in frutas:
    print(i)

print()

#append vai por na casa final
frutas.append("preto")
for i in frutas:
    print(i)

print()

#insert coloca na casa que vc digitar  
frutas.insert(2,"jaca", )
for i in frutas:
    print(i)

print()

#pop tira o elemento da casa que vc digitar
frutas.pop(2)
for i in frutas:
    print(i)

print()

#sort ordena numericamente e alfabeticamente
frutas.sort()
for i in frutas:
    print(i)

print()

